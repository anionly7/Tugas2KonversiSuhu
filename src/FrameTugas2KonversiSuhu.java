    private void hasilKonversiOtomatis() {
        if (!buttonKonversiKlik) {
            return; // ❌ Belum klik hasil, jangan konversi dulu
        }
        if (!txtInput.getText().isEmpty()
                && (rbtnCelcius.isSelected() || rbtnFahrenheit.isSelected()
                || rbtnKelvin.isSelected() || rbtnReamur.isSelected())) {
            btnKonversiActionPerformed(null);
        }
    }

    private void txtInputKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtInputKeyTyped
        char c = evt.getKeyChar();
        if (!Character.isDigit(c) && c != '.' && c != KeyEvent.VK_BACK_SPACE) {
            evt.consume();
            JOptionPane.showMessageDialog(this, "Input tidak valid! Masukkan hanya angka.", "Error", JOptionPane.ERROR_MESSAGE);
        }        // TODO add your handling code here:
    }//GEN-LAST:event_txtInputKeyTyped

