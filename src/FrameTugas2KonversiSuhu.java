    private void btnKonversiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnKonversiActionPerformed
        try {
            buttonKonversiKlik = true;
            double input = Double.parseDouble(txtInput.getText());
            String dari = cbSatuan.getSelectedItem().toString();
            double hasil = 0;
            String satuanHasil = "";

            // Menentukan tujuan konversi
            if (rbtnCelcius.isSelected()) {
                satuanHasil = "°C";
                if (dari.equals("Celcius")) {
                    hasil = input;
                } else if (dari.equals("Fahrenheit")) {
                    hasil = (input - 32) * 5 / 9;
                } else if (dari.equals("Reamur")) {
                    hasil = input * 5 / 4;
                } else if (dari.equals("Kelvin")) {
                    hasil = input - 273.15;
                }

            } else if (rbtnFahrenheit.isSelected()) {
                satuanHasil = "°F";
                if (dari.equals("Celcius")) {
                    hasil = (input * 9 / 5) + 32;
                } else if (dari.equals("Fahrenheit")) {
                    hasil = input;
                } else if (dari.equals("Reamur")) {
                    hasil = (input * 9 / 4) + 32;
                } else if (dari.equals("Kelvin")) {
                    hasil = (input - 273.15) * 9 / 5 + 32;
                }

            } else if (rbtnReamur.isSelected()) {
                satuanHasil = "°R";
                if (dari.equals("Celcius")) {
                    hasil = input * 4 / 5;
                } else if (dari.equals("Fahrenheit")) {
                    hasil = (input - 32) * 4 / 9;
                } else if (dari.equals("Reamur")) {
                    hasil = input;
                } else if (dari.equals("Kelvin")) {
                    hasil = (input - 273.15) * 4 / 5;
                }

            } else if (rbtnKelvin.isSelected()) {
                satuanHasil = "K";
                if (dari.equals("Celcius")) {
                    hasil = input + 273.15;
                } else if (dari.equals("Fahrenheit")) {
                    hasil = (input - 32) * 5 / 9 + 273.15;
                } else if (dari.equals("Reamur")) {
                    hasil = (input * 5 / 4) + 273.15;
                } else if (dari.equals("Kelvin")) {
                    hasil = input;
                }
            }

            // Tampilkan hasil
            labelOutput.setText(String.format("%.2f %s", hasil, satuanHasil));

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Input harus berupa angka!", "Error", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_btnKonversiActionPerformed

